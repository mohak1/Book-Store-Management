    public void barr(ArrayList<User> list){
        String[] barr = new String [list.size()];
        float [] parr = new float [list.size()];
        for(int i=0; i<list.size(); i++){
            barr[i] = list.get(i).getBookn();
            parr[i] = list.get(i).getSp();
        }
        User.b = barr;
        User.p = parr;
}
    
