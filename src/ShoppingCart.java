    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int[] quan = new int[20];
        String[] name = new String[900];
        int id=0;
        String cdate = curdate.getText();
        int cid = Integer.parseInt(cust.getText());
        float p = Float.parseFloat(pa.getText());
        int flag=0;
        
        
        try{
            Class.forName("java.sql.DriverManager");
            //connect with mysql
            java.sql.Connection c= java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/dbms","root","root");
            //create statement
            java.sql.Statement s= c.createStatement();
            //execute query
            
            DefaultTableModel model = (DefaultTableModel) cart.getModel();
            
            for(int i=0; i<model.getRowCount(); i++){
                quan[i] = (int) model.getValueAt(i, 1);
                name[i] = (String) model.getValueAt(i, 0);
                
                java.sql.ResultSet h = s.executeQuery("select units from main where bookname='"+name[i]+"'");
                if(h.next()){
                    int temp = Integer.parseInt(h.getString(1));
                    temp = temp-quan[i];
                    if(temp>=0){
                        //javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid quanity");
                        int k = s.executeUpdate("update main set units = '"+temp+"' where bookname = '"+name[i]+"' ");
                        java.sql.ResultSet q = s.executeQuery("select max(orderid) from orderdetails");
                        q.next();
                        String t = q.getString(1);
                        if(t.equals("Null"))
                            id = 0;
                        else{
                            id=Integer.parseInt(t);
                            id++;
                        }
                        int j = s.executeUpdate("insert into orderdetails values('"+id+"','"+cdate+"','"+cid+"','"+name[i]+" ("+quan[i]+")','"+p+"')");
                        if(j>0){
                            if(flag==0){
                            javax.swing.JOptionPane.showMessageDialog(this, "Order Placed, Check your email for more information");
                            flag=1;
                            }
                        }else{
                            javax.swing.JOptionPane.showConfirmDialog(this, "Your Order could not be placed right now.\nPlease try again later.");
                        }
                        
                    }else{
                        javax.swing.JOptionPane.showMessageDialog(this, "Order quantity invalid. Please select a lower quantity and try again.");
                    }
                }else{
                    javax.swing.JOptionPane.showMessageDialog(this, "Book not found.");
                }
            } 
        }catch(Exception e){
        javax.swing.JOptionPane.showMessageDialog(this, "Error;" + e);
    }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

